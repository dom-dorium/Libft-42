#include "libft.h"

int	ft_intlen(int n)
{
	int	i;

	if (!n)
		return (0);
	i = 0;
	if (n < 0)
	{
		i++;
		n *= -1;
	}
	while (n > 9)
	{
		n /= 10;
		i++;
	}
	i++;
	return (i);
}
